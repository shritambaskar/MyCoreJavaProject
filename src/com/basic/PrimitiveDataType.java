package com.basic;

public class PrimitiveDataType {

	public static void main(String[] args) {
		//Logical Category
		boolean isActive = true;
		
		//Textual Category
		char charValue = 'a';
		
		//Integral Category
		byte byteValue = 24;
		short shortValue = 56;
		int integerValue = 1234;
		long mobileNumber = 1234567890l;
		
		//Floating Category
		float floatValue = 89.76f;
		double doubleValue = 34.90754;
		
		System.out.println("The Value is "+isActive);
		System.out.println("The Character Value "+charValue);
		System.out.println("Byte :"+byteValue + "\nShort :"+shortValue
				+"\nInteger :"+integerValue+ "\nMobile :"+mobileNumber);
		
		String firstName = "Shrikant";
		PrimitiveDataType pdt;
		
//		float int =89f;

	}

}
